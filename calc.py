def sumFunc(num1,num2):
  return num1+num2

def subFunc(num1,num2):
  return num1-num2

def divFunc(num1,num2):
  return num1/num2

def mulFunc(num1,num2):
  return num1*num2

if __name__ == "__main__":
  print(sumFunc(1,2))
  print(subFunc(1,2))
  print(divFunc(1,2))
  print(mulFunc(1,2))

